.INCLUDE "M328PDEF.INC"
.ORG $00
JMP PROGRAMA // Reset
;Configuracion Fast Pwm, duty cicle 50%
PROGRAMA:
	LDI R16,HIGH(RAMEND)
	OUT SPH,R16
	LDI R16,LOW(RAMEND)
	OUT SPL,R16
	SBI DDRD,6 ;El pin del pwm es (PCINT22/OC0A/AIN0) PD6

;CONFIGURACION_PWM:
	LDI R25,128
	OUT OCR0A,R25
	LDI R25,0
	ORI R25,((1<<WGM01)|(1<<WGM00)|(1<<COM0A1)|(0<<COM0A0))
	OUT TCCR0A,R25
	LDI R25,0
	ORI R25,((0<<CS00)|(1<<CS01)|(0<<CS02))
	OUT TCCR0B,R25
	

FIN:rjmp FIN;
